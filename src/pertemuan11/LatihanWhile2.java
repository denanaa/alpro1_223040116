package pertemuan11;

public class LatihanWhile2 {
	/* Buatlah program menggunakan while dimana pengulangan 
	 * akan dilakukan sebanyak 10 kali. dan tampilkan kata 
	 * "Angkatan 22 jaya jaya jaya" sebanyak pengulangan
	 * tersebut
	 */
	
	public static void main (String [] args) {
		//inisialisasi nilai awal (start)
		int loop = 1;
		
		while (loop <= 10) {
			System.out.println("Angkatan 2022 jaya jaya jaya");
			loop++; //proses pengubahnya
		}
	}

}
